// binary operators

console.log(1 + 2);
console.log(2 - 1);
console.log(2 * 1);
console.log(10 / 5);
console.log(2 ** 10);

//logical operators
console.log(5 > 1);
console.log(2 < 1);
console.log(2 == 2);
console.log(1 != 2);

//bitwise operators
console.log(1 | 2);
console.log(1 & 2);
console.log(5 ^ 3);
console.log(~5);
